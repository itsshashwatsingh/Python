# Logical operator

a = True
b = False

#and logical operator
print(a and b) # False
print(a and a) # True
print(b and a) # False
print(b and b) # False

# both a & b needs to be true to get true by 'and' logical operator 

# or logical operator

print(a or b) # True
print(a or a) # True
print(b or a) # True
print(b or b) # False

# atleast one needs to be true to get true from or operator

 # Not logical operator 

print( not(True)) # false

print( not(False)) # true